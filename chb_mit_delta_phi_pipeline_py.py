import numpy as np
import pandas as pd
import matplotlib.pyplot as plt

def add_memory_integral(df, decay=0.95):
    df = df.sort_values(["file", "center_s"]).copy()
    memory_values = []

    for file_name, sub in df.groupby("file"):
        J = 0.0
        for _, row in sub.iterrows():
            J = decay * J + row["DeltaPhi"]
            memory_values.append((row.name, J))

    memory_series = pd.Series(index=df.index, dtype=float)
    for idx, val in memory_values:
        memory_series.loc[idx] = val

    df["J_memory"] = memory_series.sort_index()
    return df

