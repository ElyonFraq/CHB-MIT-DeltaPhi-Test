def shannon_entropy_1d(x, bins=64):
    x = np.asarray(x).ravel()
    hist, _ = np.histogram(x, bins=bins, density=True)
    hist = hist[hist > 0]
    if len(hist) == 0:
        return 0.0
    p = hist / hist.sum()
    return -np.sum(p * np.log(p + 1e-12))

def compute_window_features(window_data):
    """
    window_data: shape (n_channels, n_samples)
    Gibt einfache, robuste S/I/C-Features zurück.
    """

    # --------
    # S = Struktur / Signalgeometrie
    # Hier: Mittelwert aus Kanal-Varianzen + mittlere Spannweite
    # --------
    channel_vars = np.var(window_data, axis=1)
    channel_ranges = np.ptp(window_data, axis=1)  # max-min je Kanal
    S = 0.5 * np.mean(channel_vars) + 0.5 * np.mean(channel_ranges)

    # --------
    # I = Information / Entropie
    # Shannon-Entropie pro Kanal, dann Mittelwert
    # --------
    entropies = [shannon_entropy_1d(ch, bins=64) for ch in window_data]
    I = np.mean(entropies)

    # --------
    # C = Kohärenz / Kopplung
    # Hier: mittlere absolute Kanal-Korrelation (ohne Diagonale)
    # --------
    corr = np.corrcoef(window_data)
    corr = np.nan_to_num(corr, nan=0.0)
    upper = corr[np.triu_indices_from(corr, k=1)]
    C = np.mean(np.abs(upper))

    return S, I, C

