import os
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import mne


def shannon_entropy_1d(x, bins=64):
    x = np.asarray(x).ravel()
    hist, _ = np.histogram(x, bins=bins, density=True)
    hist = hist[hist > 0]
    if len(hist) == 0:
        return 0.0
    p = hist / hist.sum()
    return -np.sum(p * np.log(p + 1e-12))


def compute_window_features(window_data):
    channel_vars = np.var(window_data, axis=1)
    channel_ranges = np.ptp(window_data, axis=1)
    S = 0.5 * np.mean(channel_vars) + 0.5 * np.mean(channel_ranges)

    entropies = [shannon_entropy_1d(ch, bins=64) for ch in window_data]
    I = np.mean(entropies)

    corr = np.corrcoef(window_data)
    corr = np.nan_to_num(corr, nan=0.0)
    upper = corr[np.triu_indices_from(corr, k=1)]
    C = np.mean(np.abs(upper))

    return S, I, C


def run_pipeline(edf_path, seizure_start, seizure_end, file_tag, output_dir="outputs"):
    raw = mne.io.read_raw_edf(edf_path, preload=True, verbose=False)

    sfreq = raw.info["sfreq"]
    duration_sec = raw.n_times / sfreq
    window_sec = 10

    windows = []
    for start in np.arange(0, duration_sec, window_sec):
        end = min(start + window_sec, duration_sec)
        windows.append((float(start), float(end)))

    df_windows = pd.DataFrame(windows, columns=["win_start_s", "win_end_s"])

    SOP = 30 * 60
    SPH = 5 * 60
    POST = 30 * 60

    def label_window(row):
        center = (row["win_start_s"] + row["win_end_s"]) / 2

        if seizure_start <= center < seizure_end:
            return "ictal"

        if (seizure_start - SOP) <= center < (seizure_start - SPH):
            return "preictal"

        if seizure_end <= center < (seizure_end + POST):
            return "exclude"

        return "interictal_candidate"

    df_windows["label"] = df_windows.apply(label_window, axis=1)
    df_use = df_windows[df_windows["label"].isin(["preictal", "ictal", "interictal_candidate"])].copy()

    rows = []
    for _, row in df_use.iterrows():
        start_sample = int(row["win_start_s"] * sfreq)
        end_sample = int(row["win_end_s"] * sfreq)
        segment = raw.get_data(start=start_sample, stop=end_sample)

        S, I, C = compute_window_features(segment)

        rows.append({
            "file": file_tag,
            "win_start_s": row["win_start_s"],
            "win_end_s": row["win_end_s"],
            "label": row["label"],
            "S": S,
            "I": I,
            "C": C
        })

    df_feat = pd.DataFrame(rows)

    baseline_df = df_feat[df_feat["label"] == "interictal_candidate"].copy()
    baseline_S = baseline_df["S"].mean()
    baseline_I = baseline_df["I"].mean()
    baseline_C = baseline_df["C"].mean()

    ALPHA = 0.40
    BETA = 0.35
    GAMMA = 0.25

    df_feat["dS"] = df_feat["S"] - baseline_S
    df_feat["dI"] = df_feat["I"] - baseline_I
    df_feat["dC"] = df_feat["C"] - baseline_C

    df_feat["DeltaPhi"] = (
        ALPHA * np.abs(df_feat["dS"]) +
        BETA * np.abs(df_feat["dI"]) +
        GAMMA * np.abs(df_feat["dC"])
    )

    df_feat["center_s"] = 0.5 * (df_feat["win_start_s"] + df_feat["win_end_s"])
    df_feat["DeltaPhi_smooth"] = df_feat["DeltaPhi"].rolling(5, min_periods=1).mean()

    os.makedirs(output_dir, exist_ok=True)
    csv_path = os.path.join(output_dir, f"{file_tag}_features.csv")
    df_feat.to_csv(csv_path, index=False)

    return df_feat


